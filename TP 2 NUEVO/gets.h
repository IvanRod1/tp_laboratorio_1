#include <stdio.h>
#include <stdlib.h>
#include <string.h>
/** \brief Obtiene un numero entero
 *
 * \param mensaje[] char es un mensaje personalizado
 * \return int retorna el numero
 *
 */
int getInt(char mensaje[]);
/** \brief Obtiene un caracter
 *
 * \param mensaje[] char es un mensaje personalizado
 * \return char retorna el caracter
 *
 */
char getChar(char mensaje[]);
/** \brief Obtiene una cadena de caracteres
 *
 * \param mensaje[] char es un mensaje personalizado
 * \param palabra[] char es es array donde se va a guardar la cadena
 * \return void
 *
 */
void getString(char mensaje[],char palabra[]);
/** \brief Obtiene un numero flotante
 *
 * \param mensaje[] char es un mensaje personalizado
 * \return float retorna el numero flotante
 *
 */
float getFloat(char mensaje[]);

